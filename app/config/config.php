/*
|--------------------------------------------------------------------------
| view_file_suffix
|--------------------------------------------------------------------------
|
| 模版文件的后缀名，为空则默认为.php文件。
|
*/
$config['view_file_suffix'] = '.php';


/*
|--------------------------------------------------------------------------
| debug
|--------------------------------------------------------------------------
|
| 是否开启debug
|
*/
$config['debug'] = TRUE;


/*
|--------------------------------------------------------------------------
| 默认参数名
|--------------------------------------------------------------------------
|
