<?php  if ( ! defined('BASEPATH')) exit('No direct script access allowed');

/*
|--------------------------------------------------------------------------
| rewrite
|--------------------------------------------------------------------------
|
| 是否开启伪静态
|
*/
$config['rewrite']	= TRUE;

/*
|--------------------------------------------------------------------------
| url_suffix
|--------------------------------------------------------------------------
|
| URL的后缀名，仅在开启伪静态下此设置才生效。
